# This python program deals with arithmetic operations

# Get three integers as input from user

num1 = int(input("Enter 1st number: "))
num2 = int(input("Enter 2nd number: "))
num3 = int(input("Enter 3rd number: "))

sum_of_num = num1 + num2 + num3
print(f"Sum of all 3 numbers: {sum_of_num}")

subtract = num1 - num2
print(f"1st number minus 2nd number: {subtract}")

multiply = num3 * num2
print(f"3rd number multiplied by 1st number: {multiply}")

divide = sum_of_num / num3
print(f"Sum of all 3 numbers divided by 3rd number: {divide}")